CREATE TABLE Dept (
    DEPTNO INT(2) PRIMARY KEY,
    DNAME VARCHAR(14),
    LOC VARCHAR(13)
);

CREATE TABLE Emp (
    empno INT(4) PRIMARY KEY,
    ename VARCHAR(10),
    job VARCHAR(9),
    mgr INT(4),
    hiredate DATE,
    sal FLOAT(7 , 2 ),
    comm FLOAT(7 , 2 ),
    deptno INT(2) REFERENCES Dept
); 

